// BY USING (char) DATATYPE, WE'LL PRINT THE CHARACTERS

#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>
int main()
{
    char ch;
    scanf("%c", &ch);
    printf("The first letter of my name is: %c", ch); // %c for char datatype
    return 0;
}

// INPUT: R
// OUTPUT: The first letter of my name is: R