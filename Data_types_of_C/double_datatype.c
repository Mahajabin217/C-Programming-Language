// BY USING (double) DATATYPE, WE'LL DO SUM OF 2 VARIABLES

#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>
int main()
{
    double a, b, sum;
    a = 9.5;
    b = 8.743;
    sum = a + b;
    printf("sum is %lf", sum); // %lf for double datatype 
    return 0;
}

//OUTPUT: sum is 18.243000