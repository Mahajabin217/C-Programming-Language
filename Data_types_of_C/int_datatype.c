// BY USING (int) DATATYPE, WE'LL DO THE SUM OF 2 VARIABLES

#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>
int main()
{
    int a, b, sum;
    a = 40;
    b = 50;
    sum = a + b;
    printf("sum is %d", sum); // %d for int datatype 
    return 0;
}

// OUTPUT: sum is 90