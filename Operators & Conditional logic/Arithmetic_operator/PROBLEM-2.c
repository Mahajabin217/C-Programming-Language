// BY USING ARITHMETIC OPERATORS,WE'LL FIND OUT THE REMAINDER OF AN NUMBER IS EVEN OR ODD 

#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>
int main()
{
    int num = 5;
    int remainer = num % 2;
    if (remainder == 0)
    {
        printf("The number is even\n");
    }
    else
    {
        printf("The number is odd\n");
    }
}

// OUTPUT: The number is odd